package zadanie8_a;

public class Check {
	private int Nomer;
	private int PIN;
	private int Ostatok;
	
	public int getNomer() {
		return Nomer;
	}
	
	public void setNomer(int newNomer) {
		Nomer = newNomer;
	}
	
	public int getPIN() {
		return PIN;
	}
	
	public void setPIN(int newPIN) {
		PIN = newPIN;
	}
	
	public int getOstatok() {
		return Ostatok;
	}
	
	public void setOstatok(int newOstatok) {
		Ostatok = newOstatok;
	}
}
